#!/usr/bin/env node
// Compiles the lab's markdown source files (one directory up from website/)
// into website/api/knowledge.js — a single string the chatbot uses as its
// ONLY source of truth. The chatbot never reads the .md files at runtime;
// it reads this generated module, so it deploys cleanly to Vercel.
//
// Re-run after editing any source .md file:
//     node scripts/build-knowledge.mjs
//
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(__dirname, '../../');          // Carleton-NTNLab/ (parent of website/)
const OUT = resolve(__dirname, '../api/knowledge.js');

// Per-file character budget (null = include the whole file). The large
// reverse-chronological lists (publications, talks) keep their most RECENT
// entries, which are the ones visitors ask about. Raise a budget for more
// grounding, lower it for cheaper calls (~4 chars ≈ 1 token).
const FILES = [
  ['01-profile.md',                 null],
  ['02-research-areas.md',          null],
  ['03-research-group.md',          12000],
  ['04-publications.md',            null],   // full list — needed to answer "papers in <area>"
  ['05-magazine-papers.md',         9000],
  ['06-patents.md',                 8000],
  ['07-theses-supervised.md',       6000],
  ['08-talks-and-tutorials.md',     7000],
  ['09-awards-and-honors.md',       null],
  ['10-service-and-leadership.md',  null],
  ['11-teaching.md',                null],
  ['12-career-experience.md',       null],
  ['13-online-profiles.md',         null],
  ['14-linkedin.md',                6000],
];

function trim(text, budget) {
  text = text.trim();
  if (!budget || text.length <= budget) return text;
  // cut at the last line break before the budget so we never split an entry
  let cut = text.lastIndexOf('\n', budget);
  if (cut < budget * 0.5) cut = budget;
  return text.slice(0, cut).trim() + '\n\n…[older entries omitted from chatbot context]';
}

const parts = [];
for (const [name, budget] of FILES) {
  let raw;
  try { raw = readFileSync(resolve(SRC, name), 'utf8'); }
  catch { console.warn(`! skipped missing ${name}`); continue; }
  parts.push(trim(raw, budget));
}

const knowledge = parts.join('\n\n---\n\n');
const banner =
  `// AUTO-GENERATED by scripts/build-knowledge.mjs — do not edit by hand.\n` +
  `// Re-run "node scripts/build-knowledge.mjs" after editing the source .md files.\n` +
  `// Generated ${new Date().toISOString()} · ~${Math.round(knowledge.length / 4)} tokens\n`;

writeFileSync(OUT, `${banner}module.exports = ${JSON.stringify(knowledge)};\n`);
console.log(
  `✓ wrote ${OUT}\n  ${knowledge.length} chars (~${Math.round(knowledge.length / 4)} tokens) from ${parts.length} files`
);
